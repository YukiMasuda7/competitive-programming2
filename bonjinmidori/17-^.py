# 範囲の絞り込み問題
# N=a**b(a,b>=2, N<=10**10)
# a = 2の時, b <= 34
# 2**30>10**9, 2**4>10 より　2**34>10**10なので
# b = 2の時, a <= 10**5
# とわかるので(2<=a<=10**5, 2<=b<=34)で回せば十分

N = int(input())
# 　2**4 = 4**2のような重複は除外しないといけない
num = set()

for a in range(2, 10**5 + 1):
    for b in range(2, 34):
        if a**b <= N:
            num.add(a**b)
ans = N - len(num)
print(ans)
